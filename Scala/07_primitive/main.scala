// 1. BYTE
val b:Byte = 10 // 1 byte
println(b)

// 2. SHORT
val s:Short = 10 // 2 byte
println(s)

// 3. INT
val i = 300 // 4 byte
println(i)

// 4. LONG
val l:Long = 400 // 8 byte
println(l)

val l2:Long = 500l
println(l2)

// 5. FLOAT 4 byte
val f:Float = 34.2f
println(f)

// 6. DOUBLE (default) 8 byte
val d:Double = 2343.4f
println(d)

// 7. CHARACTER
val c:Character = 's'
println(c)

val c2 = 'd'
println(c2)

// 8. BOOLEANS
val b1:Boolean = true
println(b)

val b2 = false
println(b2)

// Scala can treat everything as an object
println(-5.abs)