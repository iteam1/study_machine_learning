public class WhileLoop{
    public static void main(String[] args){
        int i = 0;
        // while
        while ( i < 5){
            System.out.println(i);
            i++;
        }
        // do while
        int j;
        do{
            System.out.println(j);
            j++;
        }
        while(j < 5);
    }
}