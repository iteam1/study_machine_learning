public class AssigmentOperator{
    public static void main(String[] args){
        int x = 10;
        
        // = 
        x = 5;
        
        // +=
        x += 3;
        System.out.println(x);

        // -=
        x -= 3;
        System.out.println(x);

        // *=
        x  *= 3;
        System.out.println(x);

        // /=
        x /= 3;
        System.out.println(x);

        // %=
        x %= 3;
        System.out.println(x);

        // &=
        x &= 3;
        System.out.println(x);

        // |=
        x |= 3;
        System.out.println(x);

        // ^=
        x ^= 3;
        System.out.println(x);

        // >>=
        x >>= 3;
        System.out.println(x);

        // <<=
        x <<= 3;
        System.out.println(x);
    }
}